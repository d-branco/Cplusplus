#include "Harl.hpp"

int main(int argc, char **argv)
{
	if (argc != 2)
	{
		std::cerr << "Error!\nInvalid input.\n";
		return (EXIT_FAILURE);
	}

	Harl		mouth;
	std::string input = argv[1];
	mouth.complain(input);
}
